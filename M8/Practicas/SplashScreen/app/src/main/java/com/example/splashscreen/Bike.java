package com.example.splashscreen;

public class Bike {

    private int bici;
    private String textoBici;

    public Bike(int bici, String textoBici) {
        this.bici = bici;
        this.textoBici = textoBici;
    }

    public int getBici() {
        return bici;
    }

    public void setBici(int bici) {
        this.bici = bici;
    }

    public String getTextoBici() {
        return textoBici;
    }

    public void setTextoBici(String textoBici) {
        this.textoBici = textoBici;
    }
}
